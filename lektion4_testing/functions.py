def add_numbers(a, b):
    return a + b

def subtract_numbers(a, b):
    return a - b

def multiply_numbers(a, b):
    return a * b

def divide_numbers(a, b):
    return a / b

def is_even(num):
    return num % 2 == 0

def find_max(a, b):
    return max(a, b)

